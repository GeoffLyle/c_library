#include "libft.h"

void			ft_putnbr(int n)
{
	if (n < 0)
	{
		write(1, "-", 1);
		if (n < -10)
			ft_putnbr(n / -10);
		ft_putchar(-(n % 10) + '0');
	}
	if (n / 10 == 0)
		ft_putchar(n + '0');
	else
	{
		ft_putnbr(n / 10);
		ft_putchar((n % 10) + '0');
	}
}
