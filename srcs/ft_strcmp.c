int		ft_strcmp(const char *s1, const char *s2)
{
	const char	*str1;
	const char	*str2;

	str1 = s1;
	str2 = s2;
	while (*str1 && *str2 && *str1 == *str2)
	{
		str1++;
		str2++;
	}
	return (*str1 - *str2);
}
