#include <unistd.h>

void	ft_putnbr(int n)
{
	if (n < 0)
	{
		write(1, '-', 1);
		if (n == -2147483648)
		{
			write(1, '2', 1);
			n = -147483648;
		}
		n *= -1;
	}
	if (n / 10 == 0)
		write(1, n + '0', 1);
	else
	{
		write(1, n / 10, 1);
		ft_putchar(n % 10 + '0');
	}
}
