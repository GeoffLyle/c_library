#include <unistd.h>

static void    ft_putchar(char c)
{
	write(1, &c, 1);
}

void	ft_putnbr(int n)
{
	if (n < 0)
	{
		write(1, '-', 1);
		if (n == -2147483648)
		{
			write(1, '2', 1);
			n = -147483648;
		}
		n *= -1;
	}
	if (n / 10 == 0)
		ft_putchar(n);
	else
	{
		ft_putnbr(n / 10);
		ft_putchar(n % 10);
	}
}
